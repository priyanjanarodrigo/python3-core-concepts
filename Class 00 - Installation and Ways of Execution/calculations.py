# Takes two inputs from user and converts them to integers
x = int(input("Enter First Number : "))
y = int(input("Enter Second Number : "))

# Prints out addition, multiplication, division outputs
print(f'x + y = {x + y}')
print(f'x - y = {x - y}')
print(f'x * y = {x * y}')
print(f'x / y = {x / y}')
